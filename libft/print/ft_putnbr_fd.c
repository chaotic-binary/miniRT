#include "../libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		write(fd, "-", 1);
		if (n / 10 != 0)
			ft_putnbr_fd(-(n / 10), fd);
		n = -(n % 10) + '0';
	}
	else if (n >= 0)
	{
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		n = n % 10 + '0';
	}
	write(fd, &n, 1);
}
