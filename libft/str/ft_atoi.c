int	ft_atoi(char *str)
{
	long		nb;
	int			sign;

	nb = 0;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	sign = (*str == '-') ? -1 : 1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + sign * (*str++ - '0');
		if ((nb != 0) && ((nb > 0) != (sign > 0)))
			return (sign == 1 ? -1 : 0);
	}
	return ((int)nb);
}
