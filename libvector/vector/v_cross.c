#include "../../Includes/vector.h"

t_vec3	v_cross(t_vec3 u, t_vec3 v)
{
	t_vec3 res;

	res.x = u.y * v.z - u.z * v.y;
	res.y = u.z * v.x - u.x * v.z;
	res.z = u.x * v.y - u.y * v.x;
	return (res);
}
