#include "../../Includes/vector.h"

float	v_dist(t_vec3 v, t_vec3 u)
{
	return (sqrt(pow(v.x - u.x, 2) + pow(v.y - u.y, 2) +\
				pow(v.z - u.z, 2)));
}
